GREEN = RGBColor(0x1E, 0x7E, 0x34)


def h1(t):
    doc.add_heading(t, level=1)


def h2(t):
    doc.add_heading(t, level=2)


def p(t, bold=False):
    para = doc.add_paragraph()
    r = para.add_run(t)
    r.bold = bold
    return para


def bullet(t):
    doc.add_paragraph(t, style="List Bullet")


def analogy(t):
    para = doc.add_paragraph()
    r = para.add_run("🔎 비유   ")
    r.bold = True
    r.font.color.rgb = BLUE
    r2 = para.add_run(t)
